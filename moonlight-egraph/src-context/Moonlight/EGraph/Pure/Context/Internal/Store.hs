-- | Authored context-local delta: the equivalence generated by the unions
-- staged AT this exact context, kept in base-canonical coordinates. Fibers
-- are virtual — the observable relation at a context is derived by
-- accumulating authored deltas over its down-set, never stored. The revision
-- records the base epoch the coordinates were last repaired against; it is a
-- repair stamp, not semantic content, and is excluded from equality.
type ContextFiber :: Type
data ContextFiber = ContextFiber
  { cfRelation :: !(EquivalenceRelation ClassId),
    cfBaseRevision :: !EGraphRevision
  }

instance Eq ContextFiber where
  leftFiber == rightFiber =
    cfRelation leftFiber == cfRelation rightFiber

emptyContextFiberRelation :: EquivalenceRelation ClassId
emptyContextFiberRelation =
  unsafeRelationFromTotalRepMap IntSet.empty IntMap.empty

type ContextEGraph :: (Type -> Type) -> Type -> Type -> Type
data ContextEGraph f a c = ContextEGraph
  { cegBase :: EGraph f a,
    cegSite :: PreparedContextSite c,
    cegContextFibers :: Map c ContextFiber,
    cegClassSupport :: ClassSupportIndex c,
    cegContextAnalysisDeltas :: Map c (IntMap a),
    cegAnnotatedDeltaCache :: !(AnnotatedDeltaCache f),
    cegContextRevision :: !Natural,
    cegRuntimeState :: ContextRuntimeState c
  }

type AnnotatedRow :: Type
data AnnotatedRow = AnnotatedRow
  { arRootKey :: !Int,
    arChildKeys :: ![Int],
    arRegion :: !ContextRegion
  }
  deriving stock (Eq, Ord, Show)

type AnnotatedDeltaBuckets :: (Type -> Type) -> Type
data AnnotatedDeltaBuckets f = AnnotatedDeltaBuckets
  { adbVariantRowsByTag :: !(Map (f ()) [AnnotatedRow]),
    adbAbsorbedRowsByTag :: !(Map (f ()) [AnnotatedRow]),
    adbRegionalUnionFind :: !RegionalUnionFind,
    adbInhabitedRegion :: !ContextRegion
  }

deriving stock instance Eq (f ()) => Eq (AnnotatedDeltaBuckets f)

deriving stock instance Show (f ()) => Show (AnnotatedDeltaBuckets f)

type AnnotatedDeltaFrontier :: (Type -> Type) -> Type
data AnnotatedDeltaFrontier f = AnnotatedDeltaFrontier
  { adfRepresentativeKeys :: !IntSet.IntSet,
    adfVariantRowsByTag :: !(Map (f ()) (Set (Int, [Int]))),
    adfAbsorbedRowsByTag :: !(Map (f ()) (Set (Int, [Int])))
  }

deriving stock instance Eq (f ()) => Eq (AnnotatedDeltaFrontier f)

deriving stock instance Show (f ()) => Show (AnnotatedDeltaFrontier f)

type AnnotatedDeltaAdvanceMode :: Type
data AnnotatedDeltaAdvanceMode
  = AnnotatedDeltaReused
  | AnnotatedDeltaAdvancedRegionally
  | AnnotatedDeltaRecompiledForBaseRevision !EGraphRevision !EGraphRevision
  deriving stock (Eq, Ord, Show)

type AnnotatedDeltaCache :: (Type -> Type) -> Type
data AnnotatedDeltaCache f = AnnotatedDeltaCache
  { adcBaseRevision :: !EGraphRevision,
    adcContextRevision :: !Natural,
    adcBuckets :: !(AnnotatedDeltaBuckets f),
    adcDirtyFrontierByContextKey :: !(IntMap (AnnotatedDeltaFrontier f))
  }

deriving stock instance Eq (f ()) => Eq (AnnotatedDeltaCache f)

deriving stock instance Show (f ()) => Show (AnnotatedDeltaCache f)

type AnnotatedDeltaCacheAdvance :: (Type -> Type) -> Type
data AnnotatedDeltaCacheAdvance f = AnnotatedDeltaCacheAdvance
  { adcaCache :: !(AnnotatedDeltaCache f),
    adcaFrontierByContextKey :: !(IntMap (AnnotatedDeltaFrontier f)),
    adcaAdvanceMode :: !AnnotatedDeltaAdvanceMode
  }

deriving stock instance Eq (f ()) => Eq (AnnotatedDeltaCacheAdvance f)

deriving stock instance Show (f ()) => Show (AnnotatedDeltaCacheAdvance f)

type ContextRuntimeState :: Type -> Type
data ContextRuntimeState c = ContextRuntimeState
  { crsDirtyContexts :: !(Set c),
    crsLastRepair :: !(Maybe (ContextPropagationReport c))
  }

emptyContextRuntimeState :: ContextRuntimeState c
emptyContextRuntimeState =
  ContextRuntimeState
    { crsDirtyContexts = Set.empty,
      crsLastRepair = Nothing
    }

bumpContextRevision :: ContextEGraph f a c -> ContextEGraph f a c
bumpContextRevision contextGraph =
  contextGraph {cegContextRevision = succ (cegContextRevision contextGraph)}
{-# INLINE bumpContextRevision #-}

type ContextPayloadLookupError :: Type -> Type
data ContextPayloadLookupError c
  = MissingContextPayload !c
  deriving stock (Eq, Ord, Show)
